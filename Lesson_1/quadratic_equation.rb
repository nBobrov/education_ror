puts "Введите значение коэффициента a:"
a = gets.chomp.to_i

puts "Введите значение коэффициента b:"
b = gets.chomp.to_i

puts "Введите значение коэффициента c:"
c = gets.chomp.to_i

D = b**2 - (4 * a * c)

if D > 0 # например a=3, b=14, c=6
  C = Math.sqrt(D)
  x1 = (-b + C)/(2 * a)
  x2 = (-b - C)/(2 * a)
  puts "Дискриминант: #{D}, корень 1: #{x1}, корень 2: #{x2}"
elsif D==0 # например a=5, b=0, c=0
  x1 = -b/(2 * a)
  puts "Дискриминант: #{D}, корень: #{x1}"
elsif D < 0 # например a=1, b=1, c=1
  puts "Корней нет"
end